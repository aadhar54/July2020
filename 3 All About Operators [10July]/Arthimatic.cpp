#include <iostream> 
using namespace std; 

int main() { 
	int a = 10, b = 4, res; 

	// printing a and b 
	cout<<"a is "<<a<<" and b is "<<b<<"\n"; 

	// addition 
	res = a + b; 
	cout << "a+b is: "<< res << "\n"; 

	// subtraction 
	res = a - b; 
	cout << "a-b is: "<< res << "\n"; 
	
	// multiplication 
	res = a * b; 
	cout << "a*b is: "<< res << "\n"; 
	
	// division 
	res = a / b; 
	cout << "a/b is: "<< res << "\n"; 
	
	// modulus 
	res = a % b; 
	cout << "a%b is: "<< res << "\n"; 

	return 0; 
} 
